const myBase1 = parseInt(prompt('Please enter base1 of trapezoid:'))
const myBase2 = parseInt(prompt('Please enter base2 of trapezoid:'))
const tHeight = parseInt(prompt('Please enter height of trapezoid:'))
const areaOfTrapezoid = (myBase1 + myBase2 )* tHeight / 2
console.log('Area of trapezoid is:' + areaOfTrapezoid)
