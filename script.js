 var myBase1 = parseInt(prompt("Please enter base1 of trapezoid:"));
var myBase2 = parseInt(prompt("Please enter base2 of trapezoid:"));
var tHeight = parseInt(prompt("Please enter height of trapezoid:"));
var areaOfTrapezoid = (myBase1 + myBase2 ) * tHeight / 2;
console.log("Area of trapezoid is: " + areaOfTrapezoid);